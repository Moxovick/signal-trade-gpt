// ── Main ──────────────────────────────────────────────────────────────────────

async function main() {
  console.log("Wiping & re-seeding content tables…");

  await prisma.faq.deleteMany();
  await prisma.review.deleteMany();
  await prisma.prize.deleteMany();
  await prisma.legalPage.deleteMany();

  console.log(`Inserting ${FAQS.length} FAQs…`);
  await prisma.faq.createMany({ data: FAQS });

  console.log(`Inserting ${REVIEWS.length} reviews…`);
  for (const r of REVIEWS) {
    await prisma.review.create({ data: r });
  }

  console.log(`Inserting ${PRIZES.length} prizes…`);
  await prisma.prize.createMany({
    data: PRIZES.map((p) => ({ ...p, minDeposit: p.minDeposit as never })),
  });

  console.log(`Inserting ${LEGAL.length} legal pages…`);
  for (const l of LEGAL) {
    await prisma.legalPage.create({ data: l });
  }

  console.log("✓ Content seeded.");
}

main()
  .catch((e) => {
    console.error(e);
    process.exit(1);
  })
  .finally(() => prisma.$disconnect());
